# 거북이(터틀 프로그램) import turtle 파이썬에서 제공하는 터틀 프로그램
#turtle.forward 거북이가 가는 거리 #turtle.shape 거북이 모양  # turtle.done() fot문은 6장나갈때

import turtle
turtle.shape('turtle')
turtle.forward(200)
turtle.right(90)
turtle.forward(200)
turtle.right(90)
turtle.forward(200)
turtle.right(90)
turtle.forward(200)

turtle.done()
